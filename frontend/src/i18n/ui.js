function interpolate(str, vars) {
  if (!vars) return str;
  return str.replace(/\{(\w+)\}/g, (m, k) => (vars[k] != null ? String(vars[k]) : m));
}

/** Standalone translate: t(lang, key, vars). Falls back lang -> en -> key. */
export function t(lang, key, vars) {
  const dict = MESSAGES[lang] || MESSAGES.en;
  const str = (dict && dict[key] != null) ? dict[key] : (MESSAGES.en[key] != null ? MESSAGES.en[key] : key);
  return interpolate(str, vars);
}

/**
 * Hook: returns { lang, setLang, t } where t(key, vars) is bound to the current
 * UI language (the same `lang` used for quiz content, via useLang).
 */
export function useT() {
  const [lang, setLang] = useLang();
  const bound = (key, vars) => t(lang, key, vars);
  return { lang, setLang, t: bound };
}
