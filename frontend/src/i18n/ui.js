/*
 * APP UI translations (chrome only) — buttons, labels, status text, empty
 * states, helper text. This is SEPARATE from quiz CONTENT language (questions,
 * options, contest title/description, explanations), which is resolved by the
 * API. Both happen to follow the same `lang` value (from useLang), so one
 * switch moves both — but the two concerns stay separate in code: never route
 * content through these maps, and never hardcode chrome strings in components.
 *
 * Keys are flat and namespaced by prefix (dash_, team_, sess_, common_...).
 * Lookup falls back lang -> en -> the key itself, so a missing translation
 * degrades to English (or, worst case, shows the key as a visible "translate
 * me" signal) rather than blanking the UI.
 *
 * Interpolation: t('x_of_y', { n: 2, total: 5 }) replaces {n}/{total}.
 */

